library(splines)
library(splines2)
# Parte Superior 
xA = c(0.9, 1.3, 1.9, 2.1, 3.0, 3.9, 4.7, 7.0, 9.2,  11.3, 12.0, 13.0, 13.3)
yA = c(1.3, 1.5, 1.85, 2.1, 2.7, 2.4, 2.05, 2.3, 1.95, 0.9, 0.6, 0.4, 0.25)
t=length(xA)
plot(xA,yA, pch=20, cex=1, col = "red", asp=1,xlab="X", ylab="Y", main="Diagrama ",ylim= c(-7,4))
# Parte inferior cabeza
xB = c(0.82, 1, 1.5, 2.3,  3.6, 4.2,5.11, 5.53)
yB = c(1.18, 1, 1.03, 1.2, 1.1, 1.1, 0.35, 0.11)
points(xB,yB, pch=20, cex=1, col = "purple", asp=1,xlab="X", ylab="Y", main="Diagrama ")
t=t+length(xB)
# Ala superior
xC = c(4.66, 5.08, 5.39, 5.6)
yC = c(-5.16, -3.93, -2, 0.12)
points(xC,yC, pch=20, cex=1, col = "orange", asp=1,xlab="X", ylab="Y", main="Diagrama ")
t=t+length(xC)
# Ala inferior
xD = c(4.66, 4.75 , 6.6, 8,8.141)
yD = c(-5.2, -5.26,  -4.3,  -1.655,-1.14)
points(xD,yD, pch=20, cex=1, col = "green", asp=1,xlab="X", ylab="Y", main="Diagrama ")
t=t+length(xD)
# Parte inferior trasera
xE = c(8.141, 8.473, 8.8, 9.34, 9.89, 10.6, 11,  12.4,13.3)
yE = c(-1.14, -0.43, -0.51, -0.5, -0.4, -0.01, -0.09, 0.09,  0.25)
points(xE,yE, pch=20, cex=1, col = "blue", asp=1,xlab="X", ylab="Y", main="Diagrama ")
t=t+length(xE)
fA<-splinefun(xA, yA,method = c("natural"))
fB<-splinefun(xB, yB,method = c("natural"))
fC<-splinefun(xC, yC,method = c("natural"))
fD<-splinefun(xD, yD,method = c("natural"))
fE<-splinefun(xE, yE,method = c("natural"))
curve(fA,add = T,from=xA[1],to=xA[length(xA)])
curve(fB,add = T,from=xB[1],to=xB[length(xB)])
curve(fC,add = T,from=xC[1],to=xC[length(xC)])
curve(fD,add = T,from=xD[1],to=xD[length(xD)])
curve(fE,add = T,from=xE[1],to=xE[length(xE)])
